package Entidad;

/**
 *
 * @author rolo_
 */
public class Persona {
    String nombre;
    String puesto;
    String correo;
    String apellido;

    public Persona() {
    }

    public Persona(String nombre, String puesto, String correo, String apellido) {
        this.nombre = nombre;
        this.puesto = puesto;
        this.correo = correo;
        this.apellido = apellido;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPuesto() {
        return puesto;
    }

    public void setPuesto(String puesto) {
        this.puesto = puesto;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }
    
    
}
